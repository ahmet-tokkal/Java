
public class GarageDoor 
{
	public void up()
	{
		System.out.println("Door is open");
	}
	public void down()
	{
		System.out.println("Door is close");
	}
	public void stop()
	{
		System.out.println("Door is stop");
	}
	
	public void lightOn()
	{
		System.out.println("Light is on");
	}
	public void lightOff()
	{
		System.out.println("Light is off");
	}
}
